package tequila_dto;

public class CombosDTO {
	private String combo_nome;
	private int combo_desconto, combo_codigo;

	public String getCombo_nome() {
		return combo_nome;
	}

	public void setCombo_nome(String combo_nome) {
		this.combo_nome = combo_nome;
	}

	public int getCombo_desconto() {
		return combo_desconto;
	}

	public void setCombo_desconto(int combo_desconto) {
		this.combo_desconto = combo_desconto;
	}

	public int getCombo_codigo() {
		return combo_codigo;
	}

	public void setCombo_codigo(int combo_codigo) {
		this.combo_codigo = combo_codigo;
	}
}
