package tequila_dto;

public class CargosDTO {
	private int cargo_codigo;
	private String cargo_nome;
	
	public int getCargo_codigo() {
		return cargo_codigo;
	}
	public void setCargo_codigo(int cargo_codigo) {
		this.cargo_codigo = cargo_codigo;
	}
	public String getCargo_nome() {
		return cargo_nome;
	}
	public void setCargo_nome(String cargo_nome) {
		this.cargo_nome = cargo_nome;
	}
}
