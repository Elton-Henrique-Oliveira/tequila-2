package tequila_dto;

public class GruposDTO {
	private int grupo_codigo, grupo_desconto;
	private String grupo_nome;

	public int getGrupo_codigo() {
		return grupo_codigo;
	}

	public void setGrupo_codigo(int grupo_codigo) {
		this.grupo_codigo = grupo_codigo;
	}

	public int getGrupo_desconto() {
		return grupo_desconto;
	}

	public void setGrupo_desconto(int grupo_desconto) {
		this.grupo_desconto = grupo_desconto;
	}

	public String getGrupo_nome() {
		return grupo_nome;
	}

	public void setGrupo_nome(String grupo_nome) {
		this.grupo_nome = grupo_nome;
	}
}
